void update(set<int> &a, set<int> &b, int p) {
	int aL=*(--a.upper_bound(p)), aR=*a.upper_bound(p);
	distances.erase(distances.find(aR-aL-1));
	distances.insert(p-aL-1), distances.insert(aR-p-1);
	int bL=*(--b.lower_bound(p)), bR=*(++b.lower_bound(p));
	distances.erase(distances.find(p-bL-1)), distances.erase(distances.find(bR-p-1));
	distances.insert(bR-bL-1);
	b.erase(p); a.insert(p);
	cout << *distances.rbegin() << " ";
}

